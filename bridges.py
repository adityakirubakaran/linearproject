import numpy as np
from scipy.linalg import eigh  # For eigenvalue decomposition

def identify_bridges_with_linalg(adj_matrix, names):
    num_nodes = len(adj_matrix)
    bridges = []

    def laplacian_matrix(adj_matrix):
        degrees = np.sum(adj_matrix, axis=1)
        D = np.diag(degrees)
        L = D - adj_matrix
        return L

    original_laplacian = laplacian_matrix(adj_matrix)
    original_eigenvalues = eigh(original_laplacian, eigvals_only=True)
    original_fiedler_value = original_eigenvalues[1] 

    for i in range(num_nodes):
        for j in range(i + 1, num_nodes):
            if adj_matrix[i][j] == 1: 
                adj_matrix[i][j] = adj_matrix[j][i] = 0

                modified_laplacian = laplacian_matrix(adj_matrix)
                modified_eigenvalues = eigh(modified_laplacian, eigvals_only=True)
                modified_fiedler_value = modified_eigenvalues[1]

                adj_matrix[i][j] = adj_matrix[j][i] = 1

                if modified_fiedler_value > original_fiedler_value:
                    bridges.append((names[i], names[j]))

    return bridges

