import numpy as np
from itertools import combinations

def find_cliques_from_matrix(adj_matrix, names):
    # Square the adjacency matrix
    squared_matrix = np.dot(adj_matrix, adj_matrix)

    # Find cliques of size 3 (triangles)
    num_vertices = len(adj_matrix)
    cliques = []
    
    for i in range(num_vertices):
        for j in range(i + 1, num_vertices):  # Avoid double counting
            if squared_matrix[i][j] > 0:  # Shared neighbors exist
                # Verify clique condition: all neighbors are connected
                common_neighbors = [k for k in range(num_vertices) 
                                    if adj_matrix[i][k] == 1 and adj_matrix[j][k] == 1]
                for k in common_neighbors:
                    if adj_matrix[i][j] == 1 and adj_matrix[i][k] == 1 and adj_matrix[j][k] == 1:
                        cliques.append((names[i], names[j], names[k]))
    
    return cliques
    
def find_cliques_of_size_4(adj_matrix, names):
    num_vertices = len(adj_matrix)
    cliques = []

    # Check all combinations of 4 nodes
    for combination in combinations(range(num_vertices), 4):
        i, j, k, l = combination

        # Check if every pair in the combination is connected
        if (adj_matrix[i][j] == 1 and adj_matrix[i][k] == 1 and adj_matrix[i][l] == 1 and
            adj_matrix[j][k] == 1 and adj_matrix[j][l] == 1 and
            adj_matrix[k][l] == 1):
            cliques.append((names[i], names[j], names[k], names[l]))
    
    return cliques

